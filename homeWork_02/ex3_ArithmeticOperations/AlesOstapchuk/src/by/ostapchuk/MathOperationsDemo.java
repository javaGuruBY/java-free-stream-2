package by.ostapchuk;

public class MathOperationsDemo {
    public static void main(String[] args) {
        int a = 25;
        double b = 8.0;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a*b));
        System.out.println("a / b = " + (a/b));
    }
}
