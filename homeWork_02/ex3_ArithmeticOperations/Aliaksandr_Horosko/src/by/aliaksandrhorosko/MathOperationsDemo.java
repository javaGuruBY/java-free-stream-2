package by.aliaksandrhorosko;

public class MathOperationsDemo {
    public static void main(String[] args) {
        int a = 4;
        int b = 16;
        int result = a + b;
        int result1 = b - a;
        int result2 = b / a;
        int result3 = a * b;
        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
    }
}
