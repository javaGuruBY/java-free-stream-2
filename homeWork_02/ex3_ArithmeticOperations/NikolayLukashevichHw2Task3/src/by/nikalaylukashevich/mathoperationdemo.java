package by.nikalaylukashevich;

public class mathoperationdemo {

    public static void main(String[] args) {
        System.out.println("Welcome to the 'Math Operations'!");
        int a = 500;
        int b = 100;
        int addition = a + b;
        int subtraction = a - b;
        int multiplication = a * b;
        int segmentation = a / b;

        System.out.print("a = ");
        System.out.print(a);
        System.out.print(", ");
        System.out.print("b = ");
        System.out.println(b);

        System.out.print("a + b = ");
        System.out.print(a);
        System.out.print(" + ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.println(addition);

        System.out.print("a - b = ");
        System.out.print(a);
        System.out.print(" - ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.println(subtraction);

        System.out.print("a * b = ");
        System.out.print(a);
        System.out.print(" * ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.println(multiplication);

        System.out.print("a  b = ");
        System.out.print(a);
        System.out.print(" / ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.println(segmentation);
    }

}
