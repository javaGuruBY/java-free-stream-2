package by.KorshakSergey;

public class MathOperationsDemo {
    public static void main(String[] args) {
        int firstInt = 8;
        int secondInt = 2;
        int resultSum = firstInt + secondInt;
        int resultDifference = firstInt - secondInt;
        int resultMultiplication = firstInt * secondInt;
        int resultDivision = firstInt / secondInt;
        System.out.println("Пример математических вычислений тип переменных int");
        System.out.print("firstInt = ");
        System.out.println(firstInt);
        System.out.print("secondInt = ");
        System.out.println(secondInt);
        System.out.print("firstInt + secondInt = ");
        System.out.println(resultSum);
        System.out.print("firstInt - secondInt = ");
        System.out.println(resultDifference);
        System.out.print("firstInt * secondInt = ");
        System.out.println(resultMultiplication);
        System.out.print("firstInt / secondInt = ");
        System.out.println(resultDivision);

    }
}
